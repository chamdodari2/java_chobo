package java_chobo.ch06.test;

public class MyMath {
	//멤버변수
	
	int a;
	int b;
	
	public MyMath(int a, int b) {
		this.a = a;
		this.b = b;
	}

	public int add() {
		
		return a+b;
	}

	public int sub() {
		
		return a-b;
	}

	public int mul() {
	
		return a*b;
	}

	public double div() {
	
		return a/b;
	}
	
	

}
